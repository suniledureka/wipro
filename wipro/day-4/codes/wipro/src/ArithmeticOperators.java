
public class ArithmeticOperators {

	public static void main(String[] args) {
		byte b1 = 12;
		byte b2 = 13;
		//byte b3 = 25;
		//byte b3 = b1 + b2;
		int b3 = b1 + b2;
		System.out.println(b3);
		System.out.println(10 / 3); //3
		System.out.println(10f / 3);
		System.out.println(10.0 / 3);
	}

}

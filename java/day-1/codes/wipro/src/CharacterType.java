
public class CharacterType {

	public static void main(String[] args) {
		char ch = 'A';
		System.out.println(ch);
		
		ch = 65; //implicit type-casting
		System.out.println(ch);
		
		ch = '\u0041'; //unicode character
		System.out.println(ch);
		
		ch = '\u0905'; //hindi
		System.out.println(ch);
	}

}
